document.getElementById('persona-0').addEventListener('click', function () {
    const personaName = this.parentElement.querySelector('.song-title h2').textContent;
    modalBuilder(document.getElementById('modal-content'), [
        {
            type: 'div',
            class: 'popup-persona-preview-container',
            elements: [
                {
                    type: 'div',
                    class: 'popup-person-preview',
                    elements: [
                        {
                            type: 'h1',
                            txt: personaName,
                        },
                        {
                            type: 'div',
                            html: `<iframe width="100%" height="630" src="https://www.youtube-nocookie.com/embed/UwsrzCVZAb8?si=1sQJTgTD0rMcM0bw&controls=1&autoplay=1&rel=0&modestbranding=1&loop=1" title="YouTube video player" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share" referrerpolicy="strict-origin-when-cross-origin" allowfullscreen></iframe>`
                        },
                    ]
                }
            ]
        },
    ]);
    requestAnimationFrame(function () {
        openModal('modal-popup');
    });
});
