document.getElementById('persona-0').addEventListener('click', function () {
    const personaName = this.parentElement.querySelector('.song-title h2').textContent;
    modalBuilder(document.getElementById('modal-content'), [
        {
            type: 'div',
            class: 'popup-persona-preview-container',
            elements: [
                {
                    type: 'div',
                    class: 'popup-person-preview',
                    elements: [
                        {
                            type: 'span',
                            txt: personaName,
                        },
                        {
                            type: 'span',
                            txt: 'test',
                        },
                    ]
                }
            ]
        },
    ]);
    requestAnimationFrame(function () {
        openModal('modal-popup');
    });
});
